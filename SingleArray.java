import java.util.*;
public class SingleArray{
    public static void main(String args[]){
        //initialization
        int arr[]={1,2,3,4,5};
        System.out.println(Arrays.toString(arr));
        //accessing
        System.out.println(arr[2]);
        System.out.println(arr[3]);
        //modification
        arr[2]=2218;
        System.out.println(Arrays.toString(arr));
    }
}